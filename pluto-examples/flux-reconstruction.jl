# ╔═╡ 236d6cd5-c190-49c7-98fe-021fae224455
flux(y, u, edge, data) = y[1] = u[1, 1] - u[1, 2];

# ╔═╡ 6bd1c695-ff0d-46c8-bbe8-9a8e4eea9a23
md"""
At hole #11, the value will be bound to a slider defined below
